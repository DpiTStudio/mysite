"""
Модуль apps.py приложения accounts

Этот файл содержит конфигурацию приложения accounts.
Определяет метаданные приложения для Django (имя, отображаемое название и т.д.).
"""

# Импорт базового класса конфигурации приложения Django
from django.apps import AppConfig


class AccountsConfig(AppConfig):
    """
    Класс конфигурации приложения accounts.
    
    Наследуется от AppConfig и определяет настройки приложения:
    - Тип первичного ключа по умолчанию для моделей
    - Имя приложения
    - Человекочитаемые названия для админки
    """
    
    # Тип поля первичного ключа по умолчанию для всех моделей в этом приложении
    # BigAutoField - это 64-битное целое число с автоинкрементом
    # Используется вместо стандартного AutoField для поддержки больших значений
    default_auto_field = 'django.db.models.BigAutoField'
    
    # Имя приложения (должно совпадать с именем папки приложения)
    # Используется Django для идентификации приложения в системе
    name = 'accounts'
    
    # Человекочитаемое название приложения в единственном числе
    # Отображается в админке Django и других местах интерфейса
    verbose_name = 'Пользователи'
    
    # Человекочитаемое название приложения во множественном числе
    # Используется, когда нужно отобразить название во множественном числе
    verbose_name_plural = 'Пользователи'
