"""
Модуль urls.py приложения main

Этот файл определяет маршруты URL для приложения main.
Маршруты связывают URL-адреса с представлениями (views), которые обрабатывают запросы.

Все URL приложения main имеют префикс "/" в главном файле urls.py проекта
(так как main - это корневое приложение).
"""

# Импорт функции path для создания маршрутов URL
from django.urls import path
# Импорт представлений (views) приложения
from . import views

# Пространство имен приложения для использования в шаблонах и других местах
# Позволяет обращаться к маршрутам как "main:home" вместо полного пути
app_name = "main"

# Список маршрутов URL приложения
urlpatterns = [
    # Маршрут для главной страницы сайта
    # URL: / (корневой путь)
    # Обработчик: функция home из views.py
    # Имя маршрута: "home" (используется как main:home)
    path("", views.home, name="home"),
    
    # Маршрут для статических страниц
    # URL: /page/<slug>/ (например, /page/about/, /page/kontakty/)
    # <slug:slug> - параметр URL, который передается в представление как строка slug
    # Обработчик: функция page_detail из views.py
    # Имя маршрута: "page_detail" (используется как main:page_detail)
    path("page/<slug:slug>/", views.page_detail, name="page_detail"),
]
